package com.jsp.dto;

public class Student {

	private int sid;
	private String name;

	// Getters And Setters
	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}